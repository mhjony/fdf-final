#include "libft.h"

/*
** File descriptor-describes a data resource, and how that resource may be
** accessed. Standard input-0, Standart output-1, Standard error-2.
*/

void	ft_putchar_fd(char c, int fd)
{
	write(fd, &c, 1);
}
